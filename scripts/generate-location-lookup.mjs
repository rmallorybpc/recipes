function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

function summarize(locations) {
  let found = 0;
  let noCoverage = 0;
  const chains = new Set();

  for (const zip of ZIP_CODES) {
    const value = locations[zip];
    if (value === null) {
      noCoverage += 1;
      continue;
    }
    if (value && typeof value === 'object') {
      found += 1;
      if (value.chain) {
        chains.add(value.chain);
      }
    }
  }

  return {
    found,
    noCoverage,
    chainsFound: Array.from(chains).sort((a, b) => a.localeCompare(b))
  };
}

function writeOutput(locations) {
  const { found, noCoverage, chainsFound } = summarize(locations);
  const total = ZIP_CODES.length;
  const payload = {
    generated_at: new Date().toISOString(),
    total_zips: total,
    found,
    no_coverage: noCoverage,
    coverage_pct: Number(((found / total) * 100).toFixed(1)),
    chains_found: chainsFound,
    locations
  };

  writeFileSync(outputPath, `${JSON.stringify(payload, null, 2)}\n`, 'utf8');

  return payload;
}

async function authenticate(clientId, clientSecret) {
  const credentials = Buffer.from(`${clientId}:${clientSecret}`).toString('base64');
  const body = 'grant_type=client_credentials&scope=product.compact';

  const response = await fetch(TOKEN_URL, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/x-www-form-urlencoded',
      Authorization: `Basic ${credentials}`
    },
    body
  });

  if (!response.ok) {
    const text = await response.text();
    throw new Error(`Authentication failed (${response.status}): ${text}`);
  }

  const data = await response.json();
  if (!data?.access_token) {
    throw new Error('Authentication response missing access_token');
  }

  return data.access_token;
}

async function run() {
  const accessToken = await authenticate(CLIENT_ID, CLIENT_SECRET);

  let processed = 0;
  let skipped = 0;

  for (const zip of ZIP_CODES) {
    if (Object.prototype.hasOwnProperty.call(existing, zip)) {
      skipped += 1;
      processed += 1;
      console.log(`Skipping ${zip} (already in table)`);
    } else {
      try {
        const url = new URL(LOCATIONS_URL);
        url.searchParams.set('filter.zipCode.near', zip);
        url.searchParams.set('filter.radiusInMiles', '25');
        url.searchParams.set('filter.limit', '1');

        const response = await fetch(url.toString(), {
          method: 'GET',
          headers: {
            Authorization: `Bearer ${accessToken}`,
            Accept: 'application/json'
          }
        });

        if (!response.ok) {
          const body = await response.text();
          console.error(`Location API error for ${zip}: HTTP ${response.status} ${body}`);
          existing[zip] = null;
        } else {
          const data = await response.json();
          const location = data?.data?.[0];

          if (!location) {
            existing[zip] = null;
          } else {
            existing[zip] = {
              locationId: location.locationId,
              name: location.name,
              chain: location.chain,
              address: location.address?.addressLine1 || '',
              city: location.address?.city || '',
              state: location.address?.state || '',
              zip: location.address?.zipCode || zip,
              phone: location.phone || '',
              latitude: location.geolocation?.latitude || null,
              longitude: location.geolocation?.longitude || null
            };
          }
        }
      } catch (error) {
        console.error(`Location API request failed for ${zip}: ${error.message}`);
        existing[zip] = null;
      }

      processed += 1;
      await sleep(300);
    }

    if (processed % 50 === 0) {
      console.log(`Processed ${processed} / ${ZIP_CODES.length} zips...`);
    }

    if (processed % 100 === 0) {
      const { found, noCoverage } = summarize(existing);
      console.log(`Found: ${found} stores | No coverage: ${noCoverage} | Skipped: ${skipped}`);
      writeOutput(existing);
    }
  }

  const final = writeOutput(existing);

  console.log('===== Location Lookup Generation Complete =====');
  console.log(`Total zips processed: ${ZIP_CODES.length}`);
  console.log(`Stores found: ${final.found}`);
  console.log(`No coverage (null): ${final.no_coverage}`);
  console.log(`Coverage: ${final.coverage_pct}%`);
  console.log(`Chains represented: ${JSON.stringify(final.chains_found)}`);
  console.log('Output: data/kroger-locations.json');
}

run().catch((error) => {
  console.error(`Fatal error: ${error.message}`);
  process.exit(1);
});
